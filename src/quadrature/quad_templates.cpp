#include <quadrature/quad_templates.h>

void to_json  (Json& j, const QuadTemplate& p)
{
    j={
        {"type", "QuadTemplate"},
        {"nodes",   p.nodes},
        {"weights", p.weights}
    };
}
void from_json(const Json& j, QuadTemplate& p)
{
    if (j["type"] !="QuadTemplate")
        throw std::runtime_error("expected QuadTemplate, got "+j.at("type").get<std::string>() );
    p.nodes=j["nodes"].get<std::vector<real_t>>();
    p.weights=j["weights"].get<std::vector<real_t>>();
}

extern std::vector<QuadTemplate> GaussData;

QuadTemplate Rules::Gauss  (int order)
{
if ( 0<order && static_cast<size_t>(order) < GaussData.size() )
    return GaussData[order];
else throw std::runtime_error("Infeasible order.");
}


